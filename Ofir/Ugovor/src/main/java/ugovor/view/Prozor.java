    private void unesi() {
        
        ObradaKorisnik ok = new ObradaKorisnik();
        Korisnik k = new Korisnik();
        k.setIme(txtIme.getText());
        k.setPrezime(txtPrezime.getText());
        k.setEmail(txtEmail.getText());
        k.setIBAN(txtIban.getText());
        k.setKucniBroj(txtKucniBroj.getText());
        k.setUlica(txtUlica.getText());
        k.setOIB(txtOib.getText());
        
        ok.setEntitet(k);
        try {
            ok.create();
            JOptionPane.showMessageDialog(getRootPane(), "Korisnik uspje≈°no unesen");
        } catch (OfirException e) {
            lblGreska.setText(e.getPoruka());
          
        }
    }
